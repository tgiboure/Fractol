#include <unistd.h>
#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int unsigned nb;

	nb = (n < 0 ? -n : n);
	if (n < 0)
		ft_putchar_fd('-', fd);
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	ft_putchar_fd(nb % 10 + '0', fd);
}
