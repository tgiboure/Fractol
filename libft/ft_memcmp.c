#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char *p1;
	unsigned char *p2;

	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	while (n && *p1 == *p2)
	{
		n--;
		p1++;
		p2++;
	}
	if (!n)
		return (0);
	return ((unsigned char)(*p1) - (unsigned)(*p2));
}
